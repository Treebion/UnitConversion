namespace UnitConversion.ConversionUnits;

/// <summary>
/// Represents the units of speed measurement.
/// </summary>
public enum SpeedUnit
{
    /// <summary>
    /// Meters per second.
    /// </summary>
    MetresPerSecond,

    /// <summary>
    /// Kilometers per hour.
    /// </summary>
    KilometresPerHour,

    /// <summary>
    /// Miles per hour.
    /// </summary>
    MilesPerHour,

    /// <summary>
    /// Knots.
    /// </summary>
    Knots,

    /// <summary>
    /// Feet per second.
    /// </summary>
    FeetPerSecond
}

