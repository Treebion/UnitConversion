namespace UnitConversion.ConversionUnits;

/// <summary>
/// Represents the units of energy measurement.
/// </summary>
public enum EnergyUnit
{
    /// <summary>
    /// Joules.
    /// </summary>
    Joules,

    /// <summary>
    /// Kilojoules.
    /// </summary>
    Kilojoules,

    /// <summary>
    /// Calories.
    /// </summary>
    Calories,

    /// <summary>
    /// Kilocalories.
    /// </summary>
    Kilocalories,

    /// <summary>
    /// Watt-hours.
    /// </summary>
    WattHours,

    /// <summary>
    /// Kilowatt-hours.
    /// </summary>
    KilowattHours,

    /// <summary>
    /// Electron volts.
    /// </summary>
    ElectronVolts,

    /// <summary>
    /// British Thermal Units (BTU).
    /// </summary>
    BTU
}

